static void drawLitter(char litter, char block){
    int i, j;
    
    for (i = 0;i < 6;i++) {
        for (j = 0;j < 9;j++) {
            if (litters[litter][i][j])
                printChar(block);
            else
                printChar(' ');
        }
        
        newLine();
    }
}

void printTitle(char *text, char *decoration, char block){
    int i;

    for (i = 0;i < strlen(text);i++) {
        drawLitter((char)(text[i] - 97), block);
        newLine();
    }

    newLine();
}